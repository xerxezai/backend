SQL_FIX_COMMISSION = """
DO $$
DECLARE
    row_count bigint;
BEGIN
    -- Only touches mlm_commission. mlm_commissionstructure/earning/mlmprofile/transaction are
    -- never referenced here.
    IF EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_name = 'mlm_commission' AND column_name = 'earner_id'
    ) THEN
        EXECUTE 'SELECT COUNT(*) FROM mlm_commission' INTO row_count;
        IF row_count > 0 THEN
            RAISE EXCEPTION 'Legacy mlm_commission has % row(s) — aborting this migration without dropping anything. Investigate and clear/migrate this data manually before retrying.', row_count;
        END IF;
        DROP TABLE mlm_commission CASCADE;
    END IF;
END $$;

CREATE TABLE IF NOT EXISTS "mlm_commission" (
    "id" bigint NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    "level" smallint NOT NULL CHECK ("level" >= 0),
    "rate" numeric(5, 2) NOT NULL,
    "amount" numeric(14, 2) NOT NULL,
    "status" varchar(10) NOT NULL,
    "created_date" timestamp with time zone NOT NULL,
    "distributor_id" bigint NOT NULL REFERENCES "mlm_distributor" ("id") DEFERRABLE INITIALLY DEFERRED,
    "order_id" bigint NOT NULL REFERENCES "sales_salesorder" ("id") DEFERRABLE INITIALLY DEFERRED
);
CREATE INDEX IF NOT EXISTS "mlm_commission_distributor_id_cf656534" ON "mlm_commission" ("distributor_id");
CREATE INDEX IF NOT EXISTS "mlm_commission_order_id_6e360347" ON "mlm_commission" ("order_id");
"""


def copy_legacy_rates_into_settings(apps, schema_editor):
    """One-time copy of the 3 known commission-rate rows (confirmed rate-config-only, no
    transactional data) into the new MLMSettings singleton. Read-only against
    mlm_commissionstructure — never writes to or drops it."""
    with schema_editor.connection.cursor() as cursor:
        cursor.execute("""
            SELECT 1 FROM information_schema.tables WHERE table_name = 'mlm_commissionstructure'
        """)
        if not cursor.fetchone():
            return  # table doesn't exist in this environment (e.g. fresh/CI) — nothing to copy
        cursor.execute("SELECT level, commission_rate FROM mlm_commissionstructure WHERE level IN (1, 2, 3)")
        rates = dict(cursor.fetchall())

    if not rates:
        return

    MLMSettings = apps.get_model('mlm', 'MLMSettings')
    settings_obj, _ = MLMSettings.objects.get_or_create(pk=1)
    if 1 in rates:
        settings_obj.level1_rate = rates[1]
    if 2 in rates:
        settings_obj.level2_rate = rates[2]
    if 3 in rates:
        settings_obj.level3_rate = rates[3]
    settings_obj.save()


