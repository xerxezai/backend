# Isolated from Migration A on purpose: this is the one table with an actual name collision
# (mlm_commission, reused from the old MLMProfile/CommissionStructure/Transaction/Commission/
# Earning design), so it's the one place a real conflict could still block something. Keeping
# it in its own migration means a problem here can never undo Migration A's already-committed,
# risk-free table creation.
#
# mlm_commissionstructure (and the other legacy tables — mlm_earning, mlm_mlmprofile,
# mlm_transaction) are deliberately left alone entirely: nothing in this migration reads,
# writes, or drops them, except a one-time copy of the 3 known commission-rate rows into the
# new MLMSettings singleton (confirmed via Railway console: just rate config, no transactional
# data — level 1/2/3 at 10.10% / 5.00% / 2.00%). The source table itself is never touched.
#
# The `order` FK depends on 'sales' — safe to declare here since 0003 is a migration name that
# was never previously applied anywhere (unlike 0001, which already had a production-recorded
# timestamp from before any 'sales' migration existed — see feedback_migration_rewrites_are_dangerous).
